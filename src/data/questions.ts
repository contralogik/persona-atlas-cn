import type { Question, Section } from '../types'

type Item = [text: string, direction: 1 | -1]
const build = (section: Section, groups: Record<string, Item[]>): Question[] =>
  Object.entries(groups).flatMap(([dimension, items]) =>
    items.map(([text, direction], index) => ({
      id: `${section}-${dimension}-${index + 1}`,
      section,
      dimension,
      text,
      direction,
    })),
  )

